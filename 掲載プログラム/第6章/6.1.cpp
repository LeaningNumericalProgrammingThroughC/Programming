#include <stdio.h>
#include <math.h>

int main(){

	double f, g, df, dg, dh_1, dh_2, dh_3, dh_4, v_1, v_2, v_3, v_4;

	// ‰Šúİ’è ------------------------------------------

	// f(x) = xCf(4) = 4
	f = 4.0;
	df = 1.0;

	// g(x) = 3
	g = 3.0;
	dg = 0;

	// Step1 ---------------------------------------------
	v_1 = f * f;
	dh_1 = f * df + f * df;

	// Step2 ---------------------------------------------
	v_2 = sin(v_1);
	dh_2 = cos(v_1) * dh_1;

	// Step3 ---------------------------------------------
	v_3 = f + g;
	dh_3 = 1.0 * df + 1.0 * dg;

	// Step4 ---------------------------------------------
	v_4 = v_2 / v_3;
	dh_4 = 1.0 / v_3 * dh_2 - v_4 / v_3 * dh_3;

	// Œ‹‰Êo—Í-------------------------------------------
	printf("h(%.1lf) = %.3lf\n", f, v_4);
	printf("dh/dx(%.1lf) = %.3lf", f, dh_4);

	return 0;
}